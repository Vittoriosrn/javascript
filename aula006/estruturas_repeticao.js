// Contando de 1 até 10 utilizando FOR

console.log(`Utlizando o for()`)
for(var i=1; i <= 10; i++){
    console.log(`Contador: ${i}`);
}

i = 1
// Contando de 1 até 10 utilizando WHILE

console.log(`\nUtlizando o while()`)

while(i <= 10){
    console.log(`Contador: ${i}`);
    i++
}

i = 1
// Contando 1 até 10 utilizando DO-WHILE

console.log(`\nUtlizando o do{}while()`)

do{
    console.log(`Contador: ${i}`);
    i++
}while(i <= 10)